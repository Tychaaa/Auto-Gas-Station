package kkt

import "fmt"

// KKTError - ошибка, которую вернула касса в поле "Код ошибки" ответа.
type KKTError struct {
	Cmd  CommandCode
	Code byte
}

func (e *KKTError) Error() string {
	return fmt.Sprintf("ККТ вернула ошибку %s (0x%02X) на команду %s (%s)",
		errorText(e.Code), e.Code, e.Cmd.Hex(), e.Cmd.Name())
}

// errorText - расшифровка кодов ошибок ККТ A.2.0 (раздел "Коды ошибок" документа).
// Список покрывает только наиболее вероятные при работе через TCP. Если кода нет в
// мапе - возвращается "<неизвестная ошибка>".
func errorText(code byte) string {
	if t, ok := errorMessages[code]; ok {
		return t
	}
	return "<неизвестная ошибка>"
}

